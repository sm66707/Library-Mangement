    private void search(){
        String a1=jTextField1.getText();
        String sql="select * from books where BookName='"+a1+"'";
     try{
           pst=conn.prepareStatement(sql);
           rs=pst.executeQuery();
        if(rs.next()){
            jTextField2.setText(rs.getString(3));
            jTextField3.setText(rs.getString(4));
            jTextField4.setText(rs.getString(5));
            rs.close();
            pst.close();
        }
        else{ JOptionPane.showMessageDialog(null,"this book not in our library....!"); }
      }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
